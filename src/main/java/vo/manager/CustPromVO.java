/*
 * 针对客户的促销策略vo
 */
public class CustPromVO {
	public String startTime;//起始日期
	public String endTime;//结束日期
	public String type;//客户类型
	public String goodsName;//赠品名称
	public double discount;//折让金额
	public double voucher;//代金券
	//-------------------------------------------------------------
	public CustPromVO(String startTime, String endTime, String type, String goodsName, double discount, double voucher) {
		this.startTime = startTime;
		this.endTime = endTime;
		this.type = type;
		this.goodsName = goodsName;
		this.discount = discount;
		this.voucher = voucher;
	}
}
