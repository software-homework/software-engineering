package vo.finance;

import java.util.ArrayList;

public class CashExpensesVO 
{
	public String id;			//编号
	public String operator; 	//操作员
	public String account; 	//银行账户
	public ArrayList<String> list;
	public double total;		//总额
	public String time;
	public long idNum;			//编号后五位
	public int approve;
	
	public CashExpensesVO(String id, String operator, String account, ArrayList<String> list, 
			double total, String time, long idNum, int approve)
	{
		this.id = id;
		this.operator = operator;
		this.account = account;
		this.list = list;
		this.total = total;
		this.time = time;
		this.idNum = idNum;
		this.approve = approve;
	}
	
}
